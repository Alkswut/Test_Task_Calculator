package Calculator;

public class Operations {

    public static int Add (int a, int b){
        return a+b;
    }
    public static int Divide (int a, int b){
        return a/b;
    }
    public static int Minus (int a, int b){
        return a-b;
    }
    public static int Multiply (int a, int b){
        return a*b;
    }
}
